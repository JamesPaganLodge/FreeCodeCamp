// Iterate with while loops
var myArray = [];

// push the values 0,1,2,3,4 to the array
var i = 0;

while(i < 5) {
  myArray.push(i);
  i++;
}
// ------------------------------------------
// Iterate with For loops
var myArray = [];

for (var i = 1; i <= 5; i++) {
  myArray.push(i);
}
// ------------------------------------------
// Iterate odd numbers with a for loop
var myArray = [];

for (var i = 1; i < 10; i += 2) {
  myArray.push(i)
}
